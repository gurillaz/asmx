        ->with('subject:id,name,city,fiscal_no')
        ->with('price_level:id,name')
        ->select(['id','first_name', 'last_name', 'price_level_id', 'subject_id','phone_1'])
        ->get();

        return Inertia::render('Subject/Client/Index', ['clients' => $clients]);
    }

